export const lengthOk = (s, minLen = 1, maxLen) => {
  const len = String(s).length;
  let ok = len >= minLen;
  if (ok && maxLen > minLen) ok = len <= maxLen;
  return ok;
};

export const beautifyValue = fp.compose(
  fp.join(' '),
  fp.map(fp.capitalize),
  fp.split(/[-_ ]/)
);
