export const stripMargin = (mkdown) => {
  const indent = mkdown.indexOf('#') - 1;
  if (indent > 0) {
    const pattern = new RegExp(`^ {${indent}}`, 'gm');
    return mkdown.replace(pattern, '');
  } else {
    return mkdown;
  }
};

